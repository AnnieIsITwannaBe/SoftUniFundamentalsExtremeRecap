package ExamPreparation.RandomizedJudge.FinalExamSeptember2020;

public class Plant {
    public String plant;
    public int rarity;

    public Plant(String plant, int rarity) {
        this.plant = plant;
        this.rarity = rarity;
    }

    public String getPlant() {
        return plant;
    }

    public void setPlant(String plant) {
        this.plant = plant;
    }

    public int getRarity() {
        return rarity;
    }

    public void setRarity(int rarity) {
        this.rarity = rarity;
    }
}
